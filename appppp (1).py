                with pd.ExcelWriter(buffer, engine='openpyxl') as writer:
                    df_laporan.to_excel(writer, sheet_name='Laporan Manajemen', index=False)
                    df_op.to_excel(writer, sheet_name='Jadwal per Stasiun', index=False)

