# 4. KONVERSI WAKTU (MENIT EFEKTIF → JAM DINDING)
# ==========================================
def konversi_ke_jam_dinding(menit_efektif, start_date):
    """
    Konversi menit efektif (skipping Minggu & istirahat 11:30-13:00)
    ke datetime jam dinding nyata.
    Asumsi: 450 menit efektif / hari (08:30-11:30 = 180 menit, 13:00-17:30 = 270 menit)
    """
    hari_ke    = int(menit_efektif // 450)
    sisa_menit = menit_efektif % 450

    current_date  = start_date
    hari_tambahan = 0

    while hari_tambahan < hari_ke:
        current_date  += timedelta(days=1)
        if current_date.weekday() != 6:   # skip Minggu
            hari_tambahan += 1

    # Kalau landing di Minggu, geser ke Senin
    if current_date.weekday() == 6:
        current_date += timedelta(days=1)

    # sisa_menit 0–179 → sesi pagi (08:30–11:30)
    # sisa_menit 180–449 → sesi siang (13:00–17:30), skip 90 menit istirahat
    if sisa_menit <= 180:
        return current_date.replace(hour=8, minute=30, second=0, microsecond=0) + timedelta(minutes=sisa_menit)
    else:
        return current_date.replace(hour=8, minute=30, second=0, microsecond=0) + timedelta(minutes=sisa_menit + 90)


def pecah_balok_gantt(start_efektif, durasi, start_date):
    """
    Memecah blok jadwal agar tidak menembus istirahat (menit ke-180 dalam sehari)
    dan pergantian hari (menit ke-450 dalam sehari).
    """
    blocks         = []
    menit_tersisa  = durasi
    current_efektif = start_efektif

    while menit_tersisa > 0.01:
        menit_di_hari = current_efektif % 450
        if menit_di_hari < 180:
            chunk = min(menit_tersisa, 180 - menit_di_hari)
        else:
            chunk = min(menit_tersisa, 450 - menit_di_hari)

        if chunk < 0.01:
            # Tepat di batas — loncat ke segmen berikutnya
            current_efektif += 0.01
            continue

        blocks.append({
            'start_nyata'    : konversi_ke_jam_dinding(current_efektif, start_date),
            'end_nyata'      : konversi_ke_jam_dinding(current_efektif + chunk, start_date),
            'durasi_potongan': chunk
        })

        current_efektif += chunk
        menit_tersisa   -= chunk

    return blocks


