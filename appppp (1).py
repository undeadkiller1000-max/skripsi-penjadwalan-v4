# 7. SIMULATED ANNEALING (VERSI DIPERBAIKI)
# [FIX 2] Acceptance probability menggunakan current_score (bukan best_score)
# [FIX 3] Iterasi 8000, cooling 0.997 (pendinginan lambat agar eksplorasi lebih luas)
# [FIX 4] Neighbourhood: 70% swap, 30% insert (lebih variatif)
# ==========================================
def run_simulated_annealing(jobs, P_dict, D_dict, W_dict):

    def eval_sequence(seq):
        """
        Evaluasi jadwal dengan dispatcher sederhana:
        Untuk setiap job (berurutan sesuai seq), jadwalkan tiap stasiun aktif
        setelah stasiun sebelumnya (di job yang sama) dan setelah mesin itu bebas.
        """
        m_avail = {m: 0.0 for m in STATIONS}
        j_avail = {j: 0.0 for j in seq}
        total_penalti = 0
        schedule = []

        for j in seq:
            rute_aktif = [m for m in STATIONS if P_dict[j][m] > 0]
            for m in rute_aktif:
                dur   = P_dict[j][m]
                start = max(m_avail[m], j_avail[j])
                end   = start + dur
                m_avail[m] = end
                j_avail[j] = end
                schedule.append({'job': j, 'm': m, 'start': start, 'dur': dur})

            tard = max(0, j_avail[j] - D_dict[j])
            total_penalti += tard * W_dict[j]

        return total_penalti, schedule, j_avail

    def neighbour_swap(seq):
        """Tukar posisi dua job secara acak."""
        new_seq = seq.copy()
        i, j = random.sample(range(len(new_seq)), 2)
        new_seq[i], new_seq[j] = new_seq[j], new_seq[i]
        return new_seq

    def neighbour_insert(seq):
        """Cabut satu job dari posisinya, sisipkan di posisi acak lain."""
        new_seq = seq.copy()
        i = random.randrange(len(new_seq))
        job = new_seq.pop(i)
        j = random.randrange(len(new_seq) + 1)
        new_seq.insert(j, job)
        return new_seq

    # --- Inisialisasi: urutan awal = EDD (Earliest Due Date) ---
    current_seq   = sorted(jobs, key=lambda x: D_dict[x])
    current_score, _, _ = eval_sequence(current_seq)
    best_seq      = current_seq.copy()
    best_score    = current_score

    # [FIX 3] Parameter SA yang diperbaiki
    T_sa          = 500.0    # suhu awal — disesuaikan ke skala penalti
    cooling_rate  = 0.997    # pendinginan sangat lambat (dulu 0.95)
    n_iter        = 8000     # iterasi jauh lebih banyak (dulu 500)

    for _ in range(n_iter):
        # [FIX 4] Pilih operator neighbourhood secara acak
        if random.random() < 0.7:
            new_seq = neighbour_swap(current_seq)
        else:
            new_seq = neighbour_insert(current_seq)

        new_score, _, _ = eval_sequence(new_seq)

        # [FIX 2] Delta dihitung dari current_score (bukan best_score)
        delta = new_seq and (new_score - current_score)
        delta = new_score - current_score

        if delta < 0 or (T_sa > 1e-10 and random.random() < math.exp(-delta / T_sa)):
            current_seq   = new_seq
            current_score = new_score
            if new_score < best_score:
                best_seq   = new_seq.copy()
                best_score = new_score

        T_sa *= cooling_rate

    _, final_sched, final_end = eval_sequence(best_seq)
    return best_score, final_sched, final_end


