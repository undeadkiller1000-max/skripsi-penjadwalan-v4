# 5. KONVERSI DUE DATE → MENIT EFEKTIF
# [FIX 7] Deadline dihitung sampai persis jam 17:30 hari due date,
#          bukan selalu menambahkan 450 menit penuh.
# ==========================================
def hitung_target_menit(target_dt, start_dt):
    """
    Menghitung jumlah menit efektif (skip Minggu & istirahat) dari start_dt ke target_dt.
    target_dt sudah di-set ke jam 17:30 pada hari due date.
    """
    if target_dt <= start_dt:
        return 0

    total_menit = 0
    current     = start_dt

    # Hitung hari-hari penuh antara start dan due date
    while current.date() < target_dt.date():
        if current.weekday() != 6:   # skip Minggu
            total_menit += 450       # 1 hari kerja penuh = 450 menit efektif
        current += timedelta(days=1)

    # Pada hari due date, hitung hanya sampai jam 17:30 (= 450 menit efektif penuh hari itu)
    # Karena jam tutup pabrik = 17:30, dan target_dt sudah di-set 17:30,
    # maka hari terakhir = 450 menit penuh (jika bukan Minggu)
    if target_dt.weekday() != 6:
        # Hitung menit efektif dari awal hari kerja (08:30) sampai target_dt (17:30)
        # Sesi pagi: 08:30-11:30 = 180 menit; Sesi siang: 13:00-17:30 = 270 menit
        # Total = 450 menit. Tapi kalau due date jam 17:30 persis = 450 menit penuh.
        menit_di_hari_terakhir = 0
        jam_mulai_hari = current.replace(hour=8, minute=30, second=0, microsecond=0)

        if target_dt >= jam_mulai_hari:
            # Menit kalender dari 08:30 ke target_dt
            delta_kalender = (target_dt - jam_mulai_hari).total_seconds() / 60
            if delta_kalender <= 180:
                # Masih di sesi pagi
                menit_di_hari_terakhir = delta_kalender
            elif delta_kalender <= 270:
                # Di jeda istirahat — kunci ke 180 (akhir sesi pagi)
                menit_di_hari_terakhir = 180
            else:
                # Sesi siang: kurangi 90 menit istirahat
                menit_di_hari_terakhir = delta_kalender - 90

        total_menit += menit_di_hari_terakhir

    return total_menit


