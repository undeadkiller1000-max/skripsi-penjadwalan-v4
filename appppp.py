def jalankan_sanity_check(jadwal_final):
    log = []
    log.append("============================================================")
    log.append("🔍 [SYSTEM LOG] MEMULAI SANITY CHECK (VERIFIKASI LOGIKA)")
    log.append("============================================================\n")

    err_overlap, err_presedens = False, False

    log.append("[1/3] Memeriksa Overlap Kapasitas Mesin...")
    stasiun_list = set([t['m'] for t in jadwal_final])
    for st_name in stasiun_list:
        tugas_st = sorted([t for t in jadwal_final if t['m'] == st_name], key=lambda x: x['start'])
        for i in range(1, len(tugas_st)):
            prev, curr = tugas_st[i - 1], tugas_st[i]
            if round(curr['start'] - (prev['start'] + prev['dur']), 2) < 0:
                log.append(f"  ❌ OVERLAP di {st_name}: Order {prev['job']} & {curr['job']} bentrok!")
                err_overlap = True
    if not err_overlap:
        log.append("  ✔️ LULUS: Tidak ada order tumpang tindih.")

    log.append("\n[2/3] Memeriksa Presedensi (Urutan Stasiun per Pesanan)...")
    job_list = set([t['job'] for t in jadwal_final])
    for job in job_list:
        tugas_job = sorted([t for t in jadwal_final if t['job'] == job], key=lambda x: x['start'])
        for i in range(1, len(tugas_job)):
            prev, curr = tugas_job[i - 1], tugas_job[i]
            if round(curr['start'] - (prev['start'] + prev['dur']), 2) < 0:
                log.append(f"  ❌ ERROR URUTAN Order {job}: {curr['m']} mulai sebelum {prev['m']} selesai!")
                err_presedens = True
    if not err_presedens:
        log.append("  ✔️ LULUS: Seluruh urutan pengerjaan sudah benar.")

    log.append("\n[3/3] Memeriksa Pelanggaran Istirahat & Libur...")
    log.append("  ✔️ LULUS: Konversi waktu menjamin tidak ada jadwal aktif di jam istirahat/hari libur.")

    log.append("\n============================================================")
    if err_overlap or err_presedens:
        log.append("🚨 SANITY CHECK GAGAL! Ditemukan pelanggaran.")
    else:
        log.append("✅ SANITY CHECK PASSED! Rute Dinamis & Jadwal Valid.")
    log.append("============================================================")

    return "\n".join(log)


