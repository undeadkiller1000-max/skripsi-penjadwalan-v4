def hitung_waktu_proses(row, resources, setup_time):
    qty = row['qty']
    jenis = str(row['jenis produk']).lower()
    furing = row['furing']

    P = {m: 0.0 for m in STATIONS}

    # Potong
    cap_potong = 1000
    if jenis in ['kemeja', 'jaket']:
        cap_potong = 125 if furing == 1 else 250
    P['1. Potong'] = (qty / (cap_potong * resources['1. Potong'])) * 450

    # Jahit
    if jenis in ['kaos', 'polo']:
        cap_jahit = 112.5 if jenis == 'kaos' else 55
        P['2. Jahit_KaosPolo'] = (qty / (cap_jahit * resources['2. Jahit_KaosPolo'])) * 450
    elif jenis in ['kemeja', 'jaket']:
        base_cap = 13.5 if jenis == 'kemeja' else 11.0
        if furing == 1:
            base_cap *= (2 / 3)
        P['3. Jahit_KemejaJaket'] = (qty / (base_cap * resources['3. Jahit_KemejaJaket'])) * 450

    # Proses tambahan
    if row['sablon'] == 1:
        P['4. Sablon'] = (qty / (700 * resources['4. Sablon'])) * 450
    if row['dtf'] == 1:
        P['5. DTF'] = (qty / (750 * resources['5. DTF'])) * 450
    if row['bordir'] == 1:
        P['6. Bordir'] = (qty / (442.5 * resources['6. Bordir'])) * 450

    # QC
    P['7. Inspeksi_QC'] = 0.0

    # Pasang kancing
    if row['pasang kancing'] == 1 and jenis != 'kaos':
        cap_kancing = 400 if jenis == 'polo' else 125
        P['8. Pasang_Kancing'] = (qty / (cap_kancing * resources['8. Pasang_Kancing'])) * 450

    # Finishing
    cap_benang = 166.67 if furing == 1 else 500
    P['9. Buang_Benang'] = (qty / (cap_benang * resources['9. Buang_Benang'])) * 450
    P['10. Lipat'] = (qty / (500 * resources['10. Lipat'])) * 450
    P['11. Packing'] = (qty / (500 * resources['11. Packing'])) * 450

    # setup time hanya ditambahkan kalau stasiun aktif
    for m in STATIONS:
        if P[m] > 0:
            P[m] += setup_time

    return P


def konversi_ke_jam_dinding(menit_efektif, start_date):
    hari_ke = int(menit_efektif // 450)
    sisa_menit = menit_efektif % 450
    current_date = start_date
    hari_tambahan = 0

    while hari_tambahan < hari_ke:
        current_date += timedelta(days=1)
        if current_date.weekday() != 6:  # Minggu libur
            hari_tambahan += 1

    if current_date.weekday() == 6:
        current_date += timedelta(days=1)

    if sisa_menit <= 180:
        return current_date.replace(hour=8, minute=30) + timedelta(minutes=sisa_menit)
    else:
        return current_date.replace(hour=8, minute=30) + timedelta(minutes=sisa_menit + 90)


def pecah_balok_gantt(start_efektif, durasi, start_date):
    blocks = []
    menit_tersisa = durasi
    current_efektif = start_efektif

    while menit_tersisa > 0.01:
        menit_di_hari = current_efektif % 450
        if menit_di_hari < 180:
            chunk = min(menit_tersisa, 180 - menit_di_hari)
        else:
            chunk = min(menit_tersisa, 450 - menit_di_hari)

        blocks.append({
            'start_nyata': konversi_ke_jam_dinding(current_efektif, start_date),
            'end_nyata': konversi_ke_jam_dinding(current_efektif + chunk, start_date),
            'durasi_potongan': chunk
        })

        current_efektif += chunk
        menit_tersisa -= chunk

    return blocks


