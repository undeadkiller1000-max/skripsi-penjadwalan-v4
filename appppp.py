def load_order_file(uploaded_file):
    file_name = uploaded_file.name.lower()

    if file_name.endswith('.csv'):
        df = pd.read_csv(uploaded_file)
    elif file_name.endswith('.xlsx') or file_name.endswith('.xls'):
        df = pd.read_excel(uploaded_file)
    else:
        raise ValueError("Format file tidak didukung. Gunakan CSV atau Excel.")

    # rapikan nama kolom
    df.columns = df.columns.str.lower().str.strip()

    # validasi kolom wajib
    missing_cols = [col for col in REQUIRED_COLUMNS if col not in df.columns]
    if missing_cols:
        raise ValueError(f"Kolom berikut belum ada / salah nama: {missing_cols}")

    # ambil hanya kolom penting agar aman
    df = df[REQUIRED_COLUMNS].copy()

    # bersihkan string
    df['id pesanan'] = df['id pesanan'].astype(str).str.strip()
    df['jenis produk'] = df['jenis produk'].astype(str).str.strip().str.lower()

    # standardisasi nama produk
    mapping_jenis = {
        'kaos': 'kaos',
        'polo': 'polo',
        'kemeja': 'kemeja',
        'jaket': 'jaket'
    }
    df['jenis produk'] = df['jenis produk'].replace(mapping_jenis)

    unknown_jenis = sorted(set(df['jenis produk']) - set(mapping_jenis.values()))
    if unknown_jenis:
        raise ValueError(f"Jenis produk tidak dikenali: {unknown_jenis}")

    # qty numeric
    df['qty'] = pd.to_numeric(df['qty'], errors='coerce')

    # binary columns -> numeric 0/1
    for col in BINARY_COLUMNS:
        df[col] = pd.to_numeric(df[col], errors='coerce')

    # parse due date: dataset baru formatnya seperti 2/8/2025, jadi dayfirst=True
    df['due date (tanggal)'] = pd.to_datetime(
        df['due date (tanggal)'],
        errors='coerce',
        dayfirst=True
    )

    # validasi null setelah coercion
    if df[['id pesanan', 'jenis produk', 'qty', 'due date (tanggal)'] + BINARY_COLUMNS].isnull().any().any():
        bad_rows = df[df[['qty', 'due date (tanggal)'] + BINARY_COLUMNS].isnull().any(axis=1)]
        raise ValueError(
            "Ada data kosong / invalid setelah dibaca. "
            "Cek terutama kolom qty, due date (tanggal), dan kolom 0/1.\n"
            f"Baris bermasalah: {bad_rows.index.tolist()}"
        )

    # validasi qty > 0
    if (df['qty'] <= 0).any():
        bad_ids = df.loc[df['qty'] <= 0, 'id pesanan'].tolist()
        raise ValueError(f"Ada qty <= 0 pada order: {bad_ids}")

    # validasi binary harus 0/1
    for col in BINARY_COLUMNS:
        invalid_vals = df.loc[~df[col].isin([0, 1]), col].unique().tolist()
        if invalid_vals:
            raise ValueError(f"Kolom '{col}' hanya boleh berisi 0 atau 1. Nilai invalid: {invalid_vals}")

    return df


