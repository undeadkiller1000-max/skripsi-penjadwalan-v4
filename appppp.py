# 5. SIMULATED ANNEALING
# ==========================================
def run_simulated_annealing(jobs, P_dict, D_dict, W_dict):
    def eval_sequence(seq):
        m_avail = {m: 0.0 for m in STATIONS}
        j_avail = {j: 0.0 for j in seq}
        total_penalti = 0
        schedule = []

        for j in seq:
            rute_aktif = [m for m in STATIONS if P_dict[j][m] > 0]
            for m in rute_aktif:
                dur = P_dict[j][m]
                start = max(m_avail[m], j_avail[j])
                end = start + dur
                m_avail[m], j_avail[j] = end, end
                schedule.append({'job': j, 'm': m, 'start': start, 'dur': dur})

            tard = max(0, j_avail[j] - D_dict[j])
            total_penalti += tard * W_dict[j]

        return total_penalti, schedule, j_avail

    current_seq = sorted(jobs, key=lambda x: D_dict[x])
    best_seq = current_seq.copy()
    best_score, _, _ = eval_sequence(best_seq)

    T = 1000
    cooling_rate = 0.95

    for _ in range(500):
        new_seq = current_seq.copy()
        idx1, idx2 = random.sample(range(len(new_seq)), 2)
        new_seq[idx1], new_seq[idx2] = new_seq[idx2], new_seq[idx1]

        new_score, _, _ = eval_sequence(new_seq)

        if new_score < best_score or random.random() < math.exp((best_score - new_score) / T):
            current_seq = new_seq
            if new_score < best_score:
                best_seq, best_score = new_seq.copy(), new_score

        T *= cooling_rate

    _, final_sched, final_end = eval_sequence(best_seq)
    return best_score, final_sched, final_end


